package chapter2.Ex2_3;

public class CharacterEx1 {

	public static void main(String[] args) {
		char ch1 = 'A';
		System.out.println(ch1);	//A출력
		System.out.println((int)ch1);	//65출력
		
		char ch2 = 66;
		System.out.println(ch2);//B출력
		
		int ch3 = 67;
		System.out.println(ch3);	//67
		System.out.println((char)ch3);	//C

	}

}
